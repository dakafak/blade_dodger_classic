public class Gold {

    double x;
    double y;
    double size = 8;

    public Gold(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX(){
        return x;
    }

    public double getY(){
        return y;
    }

    public double getSize(){
        return size;
    }

}
